design::loadingBar(){

    SetConsoleTextAttribute(h,15); //red
    SetConsoleCP(437);
    SetConsoleOutputCP(437);
    // Initialize char for printing
    // loading bar
    char a=177;
    char l= 219;

    cout<<"\n";gotoxy(77,53);
    for (int i = 0; i < 50; i++)
        SetConsoleTextAttribute(h,15);
        cout<<a;
    // Set the cursor again starting
    // point of loading bar
    //cout<<"\t\t\t\t";
    cout<<"\r";gotoxy(77,53);
    // Print loading bar progress
    for (int i = 0; i < 54; i++) {
         SetConsoleTextAttribute(h,4);
         cout<<l;

        // Sleep for 1 second
        Sleep(10);
    //gotoxy(25,46);cout<<WHITE;system("pause");
    }

    SetConsoleTextAttribute(h,15); //white
}


design:: gotoxy(int x,int y){
    COORD coord = {0,0};
    coord.X=x;
    coord.Y=y;
    SetConsoleCursorPosition(GetStdHandle(STD_OUTPUT_HANDLE),coord);
}
